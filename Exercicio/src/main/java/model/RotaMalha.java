package model;

/**
 * Classe que representa a coordenada inicial e final com distancia
 * @author Cristiano Momesso
 *
 */
public class RotaMalha {
	private String p1;
	private String p2;
    private double distancia;
	public String getP1() {
		return p1;
	}
	public void setP1(String p1) {
		this.p1 = p1;
	}
	public String getP2() {
		return p2;
	}
	public void setP2(String p2) {
		this.p2 = p2;
	}
	public double getDistancia() {
		return distancia;
	}
	public void setDistancia(double distancia) {
		this.distancia = distancia;
	}
	
    
	
}
